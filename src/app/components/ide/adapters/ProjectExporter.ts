import JSZip from "jszip";
import { saveAs } from "file-saver";

// ── 类型 ──

export interface ExportOptions {
  projectName: string;
  includePackageJson?: boolean;
  includeTsConfig?: boolean;
  includeViteConfig?: boolean;
  includeReadme?: boolean;
}

// ── 生成默认配置文件 ──

